'''
Simples Programa que lê duas notas e calcula a média aritimética
'''
print('##### FAÇA UM PGM QUE LEIA DUAS NOTAS E CALCULE A MEDIA ARITIMETICA #####')
n1 = float(input('Qual foi a nota1?\n'))
n2 = float(input('Qual foi a nota2?\n'))
print(f'A média entre a nota {n1} e a mota {n2} é {((n1 + n2) / 2):.1f}')
